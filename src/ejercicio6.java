public class ejercicio6 {

    public static void main(String[] args) {

        System.out.println("--- Primeros 50 Múltiplos de 5 ---");

        for (int i = 1; i <= 50; i++) {

            int resultado = i * 5;

            System.out.println("5 x " + i + " = " + resultado);
        }

        System.out.println("\n--- Proceso Finalizado ---");
    }
}